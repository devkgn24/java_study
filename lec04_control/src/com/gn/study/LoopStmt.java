package com.gn.study;

public class LoopStmt {
	public static void main(String[] args) {
		// 1 ~ 10까지 숫자 모두 출력
		// 초기식 -> 출발점(1)
		// 조건식 -> 도착점(10)
		// 증감식 -> step(몇걸음씩)
		// for(초기식; 조건식; 증감식){}
		for(int i = 1 ; i <= 10 ; i++) {
			System.out.println(i);
		}
//		System.out.println(i);
	}
}
